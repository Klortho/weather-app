package gov.ncbi.WeatherApp.model;

public class Image {
	
	String title;
	Integer width;
	Integer height;
	String link;
	String url;
	public void setUrl(String url) {
		this.url = url;
	}
	public String getTitle() {
		return title;
	}
	public Integer getWidth() {
		return width;
	}
	public Integer getHeight() {
		return height;
	}
	public String getLink() {
		return link;
	}
	public String getUrl() {
		return url;
	}
}
